    public void llenarAlumno() {
        
        Integer cont=0;
        String nombre;
        for (Map.Entry <Integer,Alumno> i: alumnos.entrySet()) {
            nombre = i.getValue().getApellido() + " " + i.getValue().getNombre();
            CBAlumnos.addItem(nombre);
            alumnos2.put(cont,i.getValue());
            cont++;
        }
        
    }
    
    public void llenarMateria() {
        
        Integer cont=0;
        String materia;
        for (Map.Entry <Integer,Materia> i: materias.entrySet()) {
            materia = i.getValue().getNombre() + " " + i.getValue().getAnio();
            CBMaterias.addItem(materia);
            materias2.put(cont,i.getValue());
            cont++;
        }
        
    }
    
