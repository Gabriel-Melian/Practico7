    private void btnInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInscribirActionPerformed
        
        int valorAlumno = CBAlumnos.getSelectedIndex();
        int valorMateria = CBMaterias.getSelectedIndex();
        
        boolean inscripto = false;
        
        for (Map.Entry<Integer,Alumno> i: alumnos2.entrySet()) {
            
            if (i.getKey() == valorAlumno) {
                
                for (Map.Entry<Integer,Materia> m: materias2.entrySet()) {
                    
                    if (m.getKey() == valorMateria) {
                        inscripto = i.getValue().agregarMateria(m.getValue());
                    }
                    
                    if (inscripto == false) {
                        JOptionPane.showMessageDialog(null, "El alumno ya esta inscripto.");
                    }
                    else {
                        JOptionPane.showMessageDialog(null, "Alumno incripto con exito.");
                    }
                    
                }
            }
            
        }
        
    }//GEN-LAST:event_btnInscribirActionPerformed


